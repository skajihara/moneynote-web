#!/bin/bash
# ─────────────────────────────────────────────────────────────────────────────
# Secrets Manager からシークレットを取得して .env.{env} に書き出す
# 実行前提: EC2 に IAM_R_ka_moneynote_01 がアタッチされていること
# 使い方:
#   ./scripts/secrets-fetch.sh env1   # → .env.env1 を生成
#   ./scripts/secrets-fetch.sh env2   # → .env.env2 を生成
# ─────────────────────────────────────────────────────────────────────────────

set -euo pipefail

ENV="${1:-}"
if [[ "$ENV" != "env1" && "$ENV" != "env2" ]]; then
  echo "Usage: $0 env1|env2"
  exit 1
fi

REGION="ap-northeast-1"
REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
OUTPUT_FILE="${REPO_ROOT}/.env.${ENV}"

get_secret() {
  aws secretsmanager get-secret-value \
    --region "${REGION}" \
    --secret-id "$1" \
    --query SecretString \
    --output text \
    | python3 -c "import sys,json; print(list(json.load(sys.stdin).values())[0])"
}

get_secret_key() {
  aws secretsmanager get-secret-value \
    --region "${REGION}" \
    --secret-id "$1" \
    --query SecretString \
    --output text \
    | python3 -c "import sys,json; print(json.load(sys.stdin)['$2'])"
}

echo "Fetching secrets for ${ENV} from AWS Secrets Manager (region: ${REGION})..."

ACCOUNT_ID=$(aws sts get-caller-identity --query Account --output text)
ECR_REGISTRY=${ACCOUNT_ID}.dkr.ecr.${REGION}.amazonaws.com

if [[ "$ENV" == "env1" ]]; then
  echo "Fetching ALB DNS (alb-ka-moneynote-01)..."
  ALB_DNS=$(aws elbv2 describe-load-balancers \
    --region "${REGION}" \
    --names "alb-ka-moneynote-01" \
    --query 'LoadBalancers[0].DNSName' \
    --output text)
  FRONTEND_URL=https://${ALB_DNS}
  echo "Fetching RDS endpoint (rds-ka-moneynote-01)..."
  DB_HOST=$(aws rds describe-db-instances \
    --region "${REGION}" \
    --db-instance-identifier rds-ka-moneynote-01 \
    --query 'DBInstances[0].Endpoint.Address' \
    --output text)
  echo "Fetching ElastiCache endpoint (elc-ka-moneynote-01)..."
  REDIS_HOST=$(aws elasticache describe-replication-groups \
    --region "${REGION}" \
    --replication-group-id elc-ka-moneynote-01 \
    --query 'ReplicationGroups[0].NodeGroups[0].PrimaryEndpoint.Address' \
    --output text)
elif [[ "$ENV" == "env2" ]]; then
  echo "Fetching ALB DNS (alb-ka-moneynote-02)..."
  ALB_DNS=$(aws elbv2 describe-load-balancers \
    --region "${REGION}" \
    --names "alb-ka-moneynote-02" \
    --query 'LoadBalancers[0].DNSName' \
    --output text)
  FRONTEND_URL=https://${ALB_DNS}
  echo "Fetching RDS endpoint (RDS-ka-moneynote-02)..."
  DB_HOST=$(aws rds describe-db-instances \
    --region "${REGION}" \
    --db-instance-identifier RDS-ka-moneynote-02 \
    --query 'DBInstances[0].Endpoint.Address' \
    --output text)
  echo "Fetching ElastiCache endpoint (ELC-ka-moneynote-02)..."
  REDIS_HOST=$(aws elasticache describe-replication-groups \
    --region "${REGION}" \
    --replication-group-id ELC-ka-moneynote-02 \
    --query 'ReplicationGroups[0].NodeGroups[0].PrimaryEndpoint.Address' \
    --output text)
fi

DB_PASSWORD=$(get_secret "moneynote/${ENV}/db-password")
JWT_SECRET=$(get_secret "moneynote/${ENV}/jwt-secret")
CLAUDE_API_KEY=$(get_secret "moneynote/${ENV}/claude-api-key")
ADMIN_EMAIL=$(get_secret "moneynote/${ENV}/admin-email")

REDIS_PASSWORD=$(get_secret "moneynote/${ENV}/redis-password")

echo "Fetching SES SMTP credentials..."
MAIL_USERNAME=$(get_secret_key "moneynote/ses-smtp" "username")
MAIL_PASSWORD=$(get_secret_key "moneynote/ses-smtp" "password")
MAIL_FROM=${ADMIN_EMAIL}

# ── .env.{env} ファイルの生成 ──────────────────────────────────────────────
if [[ "$ENV" == "env1" ]]; then
  # env1: RDS・ElastiCache 構成（TLS有効・AUTH有効）
  cat > "${OUTPUT_FILE}" <<EOF
# Generated by scripts/secrets-fetch.sh ${ENV} — DO NOT COMMIT

# ── Secrets Manager から取得した機密情報 ─────────────────────
DB_PASSWORD=${DB_PASSWORD}
JWT_SECRET=${JWT_SECRET}
REDIS_PASSWORD=${REDIS_PASSWORD}
CLAUDE_API_KEY=${CLAUDE_API_KEY}
ADMIN_EMAIL=${ADMIN_EMAIL}

# ── AWS CLI で動的取得したエンドポイント ──────────────────────
DB_HOST=${DB_HOST}
REDIS_HOST=${REDIS_HOST}

# ── 固定値（機密情報ではない）────────────────────────────────
ECR_REGISTRY=${ECR_REGISTRY}
DB_PORT=5432
DB_NAME=moneynote
DB_USERNAME=moneynote
REDIS_PORT=6379
FRONTEND_URL=${FRONTEND_URL}
MAIL_HOST=email-smtp.ap-northeast-1.amazonaws.com
MAIL_PORT=587
MAIL_USERNAME=${MAIL_USERNAME}
MAIL_PASSWORD=${MAIL_PASSWORD}
MAIL_FROM=${MAIL_FROM}
EOF
elif [[ "$ENV" == "env2" ]]; then
  # env2: RDS・ElastiCache 構成（TLS有効・AUTH有効）
  cat > "${OUTPUT_FILE}" <<EOF
# Generated by scripts/secrets-fetch.sh ${ENV} — DO NOT COMMIT

# ── Secrets Manager から取得した機密情報 ─────────────────────
DB_PASSWORD=${DB_PASSWORD}
JWT_SECRET=${JWT_SECRET}
REDIS_PASSWORD=${REDIS_PASSWORD}
CLAUDE_API_KEY=${CLAUDE_API_KEY}
ADMIN_EMAIL=${ADMIN_EMAIL}

# ── AWS CLI で動的取得したエンドポイント ──────────────────────
DB_HOST=${DB_HOST}
REDIS_HOST=${REDIS_HOST}

# ── 固定値（機密情報ではない）────────────────────────────────
ECR_REGISTRY=${ECR_REGISTRY}
DB_PORT=5432
DB_NAME=moneynote
DB_USERNAME=moneynote
REDIS_PORT=6379
FRONTEND_URL=${FRONTEND_URL}
MAIL_HOST=email-smtp.ap-northeast-1.amazonaws.com
MAIL_PORT=587
MAIL_USERNAME=${MAIL_USERNAME}
MAIL_PASSWORD=${MAIL_PASSWORD}
MAIL_FROM=${MAIL_FROM}
EOF
fi

chmod 600 "${OUTPUT_FILE}"

echo "Done. Secrets written to ${OUTPUT_FILE}"
echo "DB_HOST=${DB_HOST}"
echo "REDIS_HOST=${REDIS_HOST}"
echo "FRONTEND_URL=${FRONTEND_URL}"
echo ""
echo "次のステップ:"
echo "  docker-compose -f docker-compose.${ENV}.yml up -d"
